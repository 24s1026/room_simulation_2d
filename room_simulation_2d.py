import numpy as np
import matplotlib.pyplot as plt
import imageio

nx, ny = 50, 50
nt = 130
c = 2.0
nu = 0.01

dx = 2 / (nx - 1)
dy = 2 / (ny - 1)
dt = 0.01

# 初期状態
u = np.ones((ny, nx))
wall = np.zeros((ny, nx))

# 壁（中央に設置）
wall[:, nx//2] = 1

# 壁に穴
wall[20:22, nx//2] = 0
wall[28:30, nx//2] = 0

# 左から流入
u[:, 0:5] = 1.5

plt.ioff()
fig, ax = plt.subplots()

frames = []

for n in range(nt):
    un = u.copy()

    for j in range(1, ny-1):
        for i in range(1, nx-1):

            # 壁の中は固定
            if wall[j, i] == 1:
                u[j, i] = 1
                continue

            # 左が壁ならスキップ
            if wall[j, i-1] == 1:
                continue

            # 移流＋拡散
            u[j, i] = (
                un[j, i]
                - c * dt/dx * (un[j, i] - un[j, i-1])
                - c * dt/dy * (un[j, i] - un[j-1, i])
                + nu * dt/dx**2 * (un[j, i+1] - 2*un[j, i] + un[j, i-1])
                + nu * dt/dy**2 * (un[j+1, i] - 2*un[j, i] + un[j-1, i])
            )

    # 描画
    ax.clear()
    display = u.copy()
    display[wall == 1] = 0

    ax.imshow(display, cmap='coolwarm')
    ax.set_title(f"Step {n}")

    # フレーム保存
    fig.canvas.draw()
    image = np.array(fig.canvas.renderer.buffer_rgba())
    image = image[:, :, :3]

    if n % 3 == 0:
        frames.append(image)

# GIF保存
imageio.mimsave('simulation_wall_hole.gif', frames, fps=5)

plt.close()

