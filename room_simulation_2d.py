"""**壁を設置する**"""

import numpy as np
import matplotlib.pyplot as plt
import imageio

nx, ny = 50, 50
nt = 130
c = 2.0
nu = 0.01

dx = 2 / (nx - 1)
dy = 2 / (ny - 1)
dt = 0.01

# 初期状態
u = np.ones((ny, nx))
wall = np.zeros((ny, nx))

# 壁（中央）
wall[:, nx//2] = 1

# 穴（必要なら消すと完全な壁）
# wall[20:22, nx//2] = 0
# wall[28:30, nx//2] = 0

# 左から流入
u[:, 0:5] = 1.5

plt.ioff()
fig, ax = plt.subplots()

frames = []

for n in range(nt):
    un = u.copy()

    for j in range(1, ny-1):
        for i in range(1, nx-1):

            # 壁の中は固定
            if wall[j, i] == 1:
                u[j, i] = 1
                continue

            # 周囲の壁チェック
            left_wall  = wall[j, i-1] == 1
            right_wall = wall[j, i+1] == 1
            up_wall    = wall[j-1, i] == 1
            down_wall  = wall[j+1, i] == 1

            # 移流（壁なら止める）
            adv_x = 0 if left_wall else c * dt/dx * (un[j, i] - un[j, i-1])
            adv_y = 0 if up_wall   else c * dt/dy * (un[j, i] - un[j-1, i])

            # 拡散（壁の影響を考慮）
            diff_x = 0
            if not left_wall and not right_wall:
                diff_x = nu * dt/dx**2 * (un[j, i+1] - 2*un[j, i] + un[j, i-1])

            diff_y = 0
            if not up_wall and not down_wall:
                diff_y = nu * dt/dy**2 * (un[j+1, i] - 2*un[j, i] + un[j-1, i])

            # 更新
            u[j, i] = un[j, i] - adv_x - adv_y + diff_x + diff_y

    # 描画
    ax.clear()
    display = u.copy()
    display[wall == 1] = 0

    ax.imshow(display, cmap='coolwarm')
    ax.set_title(f"Step {n}")

    # フレーム保存
    fig.canvas.draw()
    image = np.array(fig.canvas.renderer.buffer_rgba())
    image = image[:, :, :3]

    if n % 3 == 0:
        frames.append(image)

# GIF保存
imageio.mimsave('simulation_wall.gif', frames, fps=5)

plt.close()

print("GIF保存完了: simulation_wall.gif")

