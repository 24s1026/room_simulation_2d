"""**部屋の空気の流れ**"""

import numpy as np
import matplotlib.pyplot as plt
import imageio

nx, ny = 50, 50
nt = 130
c = 2.0
nu = 0.01

dx = 2 / (nx - 1)
dy = 2 / (ny - 1)
dt = 0.01

# 初期状態
u = np.ones((ny, nx))

# 左から流入
u[:, 0:5] = 1.5

plt.ioff()
fig, ax = plt.subplots()

frames = []

for n in range(nt):
    un = u.copy()

    for j in range(1, ny-1):
        for i in range(1, nx-1):

            # 基本の移流＋拡散
            u[j, i] = (
                un[j, i]
                - c * dt/dx * (un[j, i] - un[j, i-1])
                - c * dt/dy * (un[j, i] - un[j-1, i])
                + nu * dt/dx**2 * (un[j, i+1] - 2*un[j, i] + un[j, i-1])
                + nu * dt/dy**2 * (un[j+1, i] - 2*un[j, i] + un[j-1, i])
            )

    # 描画
    ax.clear()
    ax.imshow(u, cmap='coolwarm')
    ax.set_title(f"Step {n}")

    # フレーム保存
    fig.canvas.draw()
    image = np.array(fig.canvas.renderer.buffer_rgba())
    image = image[:, :, :3]

    if n % 3 == 0:
        frames.append(image)

# GIF保存
imageio.mimsave('simulation.gif', frames, fps=5)

plt.close()

print("GIF保存完了: simulation.gif")

